<h2>Tentang A Store</h2>
<p>"A Store berdiri sejak tahun 2018. Nama A Store diambil dari nama pemilik toko yaitu Ayy dan Ardian.
A Store merupakan toko online yang menyediakan fashion cassual mulai dari Hoddie, kaos, kemeja, topi hingga sepatu.
Brand-brand cassual seperti Adidas, Fila, Polo dan lainlain ini dapat menarik minat anak muda jaman sekarang."</p>
